export const getLocalizedDateTime = (stringDate: string) => {
    return stringDate
        .replace('January', 'Январь')
        .replace('january', 'Январь')
        .replace('February', 'Февраль')
        .replace('february', 'Февраль')
        .replace('March', 'Март')
        .replace('march', 'Март')
        .replace('April', 'Апрель')
        .replace('april', 'Апрель')
        .replace('May', 'Май')
        .replace('may', 'Май')
        .replace('June', 'Июнь')
        .replace('june', 'Июнь')
        .replace('July', 'Июль')
        .replace('july', 'Июль')
        .replace('August', 'Август')
        .replace('august', 'Август')
        .replace('September', 'Сентябрь')
        .replace('september', 'Сентябрь')
        .replace('October', 'Октябрь')
        .replace('october', 'Октябрь')
        .replace('November', 'Ноябрь')
        .replace('november', 'Ноябрь')
        .replace('December', 'Декабрь')
        .replace('december', 'Декабрь')
        .replace('weeks', 'недели')
        .replace('month', 'месяца')
        .replace('ago', 'назад')
}